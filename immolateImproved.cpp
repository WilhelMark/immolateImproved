#include <iostream>

using namespace std;

int main() {
    // Переменные для хранения здоровья орка и его сопротивляемости
    float health, resistance;

    // Ввод начальных значений здоровья и сопротивляемости
    cout << "Введите здоровье орка (от 0 до 1): ";
    cin >> health;
    cout << "Введите сопротивляемость магии орка (от 0 до 1): ";
    cin >> resistance;

    // Проверка корректности ввода
    if (health < 0 || health > 1 || resistance < 0 || resistance > 1) {
        cout << "Ошибка: значения должны быть в диапазоне от 0 до 1." << endl;
        return 1; // Завершаем программу с ошибкой
    }

    float damage; // Переменная для хранения мощности огненного шара

    // Цикл, который продолжается пока здоровье орка больше нуля
    while (health > 0) {
        cout << "Введите мощность огненного шара (от 0 до 1): ";
        cin >> damage;

        // Проверка корректности ввода мощности
        if (damage < 0 || damage > 1) {
            cout << "Ошибка: мощность должна быть в диапазоне от 0 до 1." << endl;
            continue; // Пропускаем итерацию цикла
        }

        // Расчёт фактического урона с учётом сопротивляемости
        float effectiveDamage = damage * (1 - resistance);

        // Уменьшаем здоровье орка на фактический урон
        health -= effectiveDamage;

        // Если здоровье стало меньше нуля, устанавливаем его в ноль
        if (health < 0) {
            health = 0;
        }

        // Выводим информацию о результате удара
        cout << "Нанесённый урон: " << effectiveDamage << endl;
        cout << "Осталось здоровья у орка: " << health << endl;

        // Проверяем, погиб ли орк
        if (health == 0) {
            cout << "Орк погиб!" << endl;
            break; // Завершаем цикл, если орк погиб
        }
    }

    return 0; // Завершение программы
}
